// ResolveTemplate возвращает тело canonical-шаблона по имени пресета.
// Второй параметр ok=false означает, что имени в registry нет - caller
// должен трактовать входящий template_name как сырое тело шаблона (старое
// поведение, сохраняющее совместимость с check_12).
func ResolveTemplate(name string) (string, bool) {
	body, ok := canonicalTemplates[name]
	return body, ok
}

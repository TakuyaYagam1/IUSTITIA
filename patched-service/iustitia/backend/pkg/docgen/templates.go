// Canonical document templates (summons/indictment/verdict).
//
// Резолвинг через этот registry отделяет имя пресета (которое шлёт фронт
// кнопкой "Повестка / Обвинительное / Приговор") от самого тела шаблона,
// которое подставляется из сервера. Это сознательная архитектурная граница:
//
//   1. F3 (SSTI) требует, чтобы userTemplate не парсился Go text/template.
//      Registry это не меняет - тело всё равно уходит в docgen.Generate,
//      где разрешена только whitelist-подстановка {{case_id}} / {{defendant}}
//      / {{verdict}} / {{details}}. Никакого {{.Method}}, FuncMap, pipe.
//
//   2. Фронт шлёт короткий ключ (summons), а не всю простыню - бандл
//      остаётся лёгким; Сменить формулировки "официального языка" можно
//      тут без пересборки фронта.
//
//   3. Если template_name НЕ из registry - падаем на userTemplate как есть
//      (старое поведение). Это нужно для обратной совместимости с чекером
//      check_12 (e2e round-trip), который может слать "произвольный"
//      template_name и не должен ломаться.

